    void animate_bocchi(void) {
        /* clear */
        oled_set_cursor(BOCCHI_FRAME_X, BOCCHI_FRAME_Y - 1);
        oled_write("     ", false);
        oled_set_cursor(BOCCHI_FRAME_X, BOCCHI_FRAME_Y);

        current_bocchi_frame = (current_bocchi_frame + 1) % BOCCHI_FRAME_COUNT;
        oled_write_raw_P(bocchi_with_guitar[current_bocchi_frame], BOCCHI_FRAME_SIZE);
    }

    /* animation timer */
    if (timer_elapsed32(anim_bocchi_timer) > BOCCHI_FRAME_DURATION) {
        anim_bocchi_timer = timer_read32();
        animate_bocchi();
    }
